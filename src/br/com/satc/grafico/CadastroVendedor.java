    private void jBSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSalvarActionPerformed

        String nome,cpf,rg,especialidade,cargo,setor;
        double salario;
        if(!(jTFCpf.getText().equals("") ||
            jTFNome.getText().equals("") ||
            jTFRenda.getText().equals("") ||
            jTFEspecialidade.getText().equals("") ||
            jTFSetor.getText().equals("") ||
            jTFCargo.getText().equals("") ||
            jTFRg.getText().equals(""))){
        nome = jTFNome.getText();
        cpf = jTFCpf.getText();
        rg = jTFRg.getText();
        especialidade =  jTFEspecialidade.getText();
        setor = jTFSetor.getText();
        cargo =  jTFCargo.getText();
        try{
            salario = Double.parseDouble (jTFRenda.getText());
            BancoVendedor.getInstance().vendedores.add(new Vendedor(especialidade,setor, cargo, salario, nome,  rg, cpf));
            jTFNome.setText("Nome");
            jTFCpf.setText("CPF");
            jTFRg.setText("RG");
            jTFRenda.setText("Renda");
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null,"Valor da renda inv√°lido!!\n"+nfe);
        }
        }else{
            JOptionPane.showMessageDialog(null,"Campo em branco!!");
        }
    }//GEN-LAST:event_jBSalvarActionPerformed

